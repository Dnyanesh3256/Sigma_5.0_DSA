// 1) Bubble Sort
//     1.1) Time Complexity =>
//         I) Best Case => O(n)
//         II) Worst Case => O(n^2)


// 2) Binary Search
//     1.1) Time Complexity =>
//         I) Best Case => O(log n)


// 3) Factorial using Recursion, Sum of N numbers using Recursion
//     1.1) Time Complexity =>
//         I) Best Case => O(n)
//
//     2.1) Space Complexity =>
//         I) Best Case => O(n)


// 4) Fibonacci Number using Recursion
//     1.1) Time Complexity =>
//         Best Case => O(2^n)

//     2.1) Space Complexity



public class TimeAndSpaceComplexitiesOfAlgorithms {
    public static void main(String[] args) {
        System.out.println("Bubble Sort : ");
        System.out.println("Time Complexity : ");
        System.out.println("Best Case : O(n)");
        System.out.println("Worst Case : O(n^2)");
        System.out.println();

        System.out.println("Binary Search : ");
        System.out.println("Time Complexity : O(log n)");
        System.out.println();

        System.out.println("Factorial using Recursion : ");
        System.out.println("Time Complexity : O(n)");
        System.out.println("Space Complexity : O(n)");
        System.out.println();

        System.out.println("Sum of N numbers using Recursion : ");
        System.out.println("Time Complexity : O(n)");
        System.out.println("Space Complexity : O(n)");
        System.out.println();

        System.out.println("Fibonacci number using Recursion : ");
        System.out.println("Time Complexity : O(2^n)");
        System.out.println("Space Complexity : O(n)");
        System.out.println();

        System.out.println("Merge Sort using Recursion : ");
        System.out.println("Time Complexity : O(n log n)");
        System.out.println("Space Complexity : O(n)");
        System.out.println();

        System.out.println("Power Function using Recursion I : ");
        System.out.println("Time Complexity : O(n)");
        System.out.println("Space Complexity : O(n)");
        System.out.println();

        System.out.println("Power Function using Recursion II : ");
        System.out.println("Time Complexity : O(n)");
        System.out.println("Space Complexity : O(n)");
        System.out.println();

        System.out.println("Power Function using Recursion III : ");
        System.out.println("Time Complexity : O(log n)");
        System.out.println("Space Complexity : O(n)");
        System.out.println();
    }
}
